package Operators;

public class Relational {
	public static void main(String args[]) {
		int a = 7, b = 11;

	    System.out.println("a is " + a + " and b is " + b);

	    // == operator
	    System.out.println(a == b); 

	    // != operator
	    System.out.println(a != b); 

	    // > operator
	    System.out.println(a > b); 

	    // < operator
	    System.out.println(a < b); 

	    // >= operator
	    System.out.println(a >= b); 

	    // <= operator
	    System.out.println(a <= b); 
	}
}
